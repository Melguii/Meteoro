package Pokemon.Especial.Legendario;

public class Location {

    private float longitude;
    private float latitude;

    /**
     * Getter de longitude
     * @return Devuelve la longitude de location
     */
    public float getLongitude() {
        return longitude;
    }

    /**
     * Setter de longitud
     * @param longitude Inserta la longitude en location
     */
    public void setLongitude(float longitude) {
        this.longitude = longitude;
    }

    /**
     * Getter de latitude
     * @return Devuelve la latitude de location
     */
    public float getLatitude() {
        return latitude;
    }

    /**
     * Setter de latitude
     * @param latitude Inserta la latitude en location
     */
    public void setLatitude(float latitude) {
        this.latitude = latitude;
    }

}