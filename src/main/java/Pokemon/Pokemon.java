package Pokemon;

import Pokemon.Especial.Legendario.Gym;
import Pokemon.Especial.Legendario.Legendario;

public  abstract class Pokemon {


    private String name;
    private int id;
    private int capture_rate;
    private String front_default;
    private String flavor_text;
    private int weight;

    public String getFlavor_text() {
        return flavor_text;
    }

    public void setFlavor_text(String flavor_text) {
        this.flavor_text = flavor_text;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getBase_experience() {
        return base_experience;
    }

    public void setBase_experience(int base_experience) {
        this.base_experience = base_experience;
    }

    private int height;
    private int base_experience;


    public String getFront_default() {
        return front_default;
    }

    public void setFront_default(String front_default) {
        this.front_default = front_default;
    }


    public int getCapture_rate() {
        return capture_rate;
    }

    public void setCapture_rate(int capture_rate) {
        this.capture_rate = capture_rate;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public abstract double captureEcuation(double pb);


    private Legendario legendario;

    public Gym getGymLegendario(){
        if (legendario instanceof Legendario){
            return legendario.getGym();
        }
        return null;
    }

}